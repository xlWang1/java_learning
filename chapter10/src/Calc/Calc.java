package Calc;

public class Calc{
    private double a;
    private double b;

    public Calc(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double jiafa(double a, double b){
        return a+b;
    }
    public double jianfa(double a,double b){
        return a-b;
    }
    public double chengfa(double a,double b){
        return a*b;
    }
    public double chufa(double a,double b){
        return a/b;
    }



}
